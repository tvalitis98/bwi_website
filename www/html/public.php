<h2 id="about">About Us</h2>

<center><p>The mission the Building-Wide Intelligence Project is to develop fully autonomous mobile robots that can become a permanent part of a building's environment. Working in the intersection of Artificial Intelligence and Robotics, our aim is to create robots that are useful platforms for groundbreaking research, while at the same time are also helpful for humans. Some prominent areas of research include reasoning, planning, human-robot interaction, grounded language learning, and multi-robot coordination.</p></center>

