<h2 id="research">Research</h2>

<ul>
	<li><h4>Reasoning and Planning for Mobile Robots</h4>
	<p>To be deemed intelligent in human-robot coexisting environments, robots need the capability of representing and reasoning with commonsense knowledge. Such knowledge is normally true, but not always. For instance, people prefer coffee in the morning; and office doors are closed during the holidays. We use answer set programming (ASP) and its extensions for commonsense reasoning and partially observable Markov decision processes (POMDPs) for probabilistic planning. The goal of this research is to develop algorithms to enable robots to plan under uncertainty while reason with commonsense knowledge at the same time.</p>
	<p>Representative Publication: <a href="http://www.cs.utexas.edu/~pstone/Papers/bib2html-links/AAAI15-szhang.pdf">CORPP: Commonsense Reasoning and Probabilistic Planning, as Applied to Dialog with a Mobile Robot. (AAAI 2015)</a></p>
	
	</li>
	<li><h4>Human-Robot Interaction</h4>
	<p>
	Robots are becoming increasingly sophisticated, and are bound to become per-vasive in humans’ every-day lives. To effectively collaborate with humans, it is useful for a robot to understand their activities and intentions automatically. This understanding is especially important in human-robot interaction scenarios: if the robot can properly interpret the behavior of humans, its communication with them will be facilitated, and its ability to interact with them will improve. Therefore, the aim of this research is to develop new methods that would allow a robot to effectively recognize human activities, intentions, etc. and appropriately react to them. 
	</p>
	<p>Representative Publication: <a href="http://www.cs.utexas.edu/~pstone/Papers/bib2html-links/ICSR2015-gori.pdf">Robot-centric activity recognition ‘in the wild’. (ICSR 2015)</a></p>
	</li>
	
	
	<li><h4>Multi-Robot Coordination and Guidance</h4><p>
	In this research, we demonstrate how individual service robots in a multi-robot system can be temporarily reassigned from their original task to help guide a human from one location to another in the environment. We formulate this multi-robot treatment of the human guidance problem as a Markov Decision Process (MDP), and explore how different MDP planners can be used to solve this problem. Our long term goal for this research is to expand the MDP to a general framework for efficiently interrupting robots performing background service tasks to efficiently aid humans as necessary.
	</p>
	<p>Representative Publication: <a href="http://www.cs.utexas.edu/~pstone/Papers/bib2html-links/AAMAS15-khandelwal.pdf">Leading the Way: An Efficient Multi-robot Guidance System (AAMAS 2015)</a></p>
	</li>
</ul>

